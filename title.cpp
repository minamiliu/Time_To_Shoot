/*******************************************************************************
関数名:	void InitTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの初期化処理
*******************************************************************************/
void InitTitle(void)
{
	InitTitleBG();
	InitTitleMenu();
	InitClock(D3DXVECTOR3(SCREEN_WIDTH/2-10, 140, 0));
}

/*******************************************************************************
関数名:	void UpdateTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの更新関数
*******************************************************************************/
void UpdateTitle(void)
{
	if(GetKeyboardTrigger(DIK_RETURN) || GetGamePadTrigger( 0, BUTTON_START))
	{
		FadeOutToNext(MODE_GAME);
	}
	UpdateClock(1.0f);
	UpdateTitleMenu();
}

/*******************************************************************************
関数名:	void DrawTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの描画関数
*******************************************************************************/
void DrawTitle(void)
{
	DrawTitleBG();
	DrawClock();
	DrawTitleMenu();
}
/*******************************************************************************
関数名:	void UninitTitle(void)
引数:	なし
戻り値:	なし
説明:	ゲームタイトルの開放関数
*******************************************************************************/
void UninitTitle(void)
{
	UninitTitleBG();
	UninitClock();
	UninitTitleMenu();
}


