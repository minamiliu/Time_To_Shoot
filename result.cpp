/*******************************************************************************
関数名:	void InitResult(void)
引数:	なし
戻り値:	なし
説明:	リザルトの初期化処理
*******************************************************************************/
void InitResult(void)
{
	InitResultBG();
	SetMessage(MSG_RANKING);

	InitResultRank();

}

/*******************************************************************************
関数名:	void UpdateResult(void)
引数:	なし
戻り値:	なし
説明:	リザルトの更新関数
*******************************************************************************/
void UpdateResult(void)
{
	UpdateResultBG();

	if(GetKeyboardTrigger(DIK_RETURN) || GetGamePadTrigger( 0, BUTTON_START))
	{
		//SetMode(MODE_TITLE);
		FadeOutToNext(MODE_TITLE);
	}

	UpdateResultRank();

}

/*******************************************************************************
関数名:	void DrawResult(void)
引数:	なし
戻り値:	なし
説明:	リザルトの描画関数
*******************************************************************************/
void DrawResult(void)
{
	DrawResultBG();
	DrawResultRank();
	
}
/*******************************************************************************
関数名:	void UninitResult(void)
引数:	なし
戻り値:	なし
説明:	リザルトの開放関数
*******************************************************************************/
void UninitResult(void)
{
	ClearMessage(MSG_RANKING);
	UninitResultBG();
	UninitResultRank();
}
